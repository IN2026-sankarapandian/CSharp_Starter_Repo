namespace FilesAndStreams.Enums;

/// <summary>
/// Specifies the type of message for displaying.
/// </summary>
public enum MessageType
{
    /// <summary>
    /// Represents the prompt message that requests user's input.
    /// </summary>
    Prompt,

    /// <summary>
    /// Represents the information message that informs to user.
    /// </summary>
    Information,

    /// <summary>
    /// Represents the title shown to the user.
    /// </summary>
    Title,
}
