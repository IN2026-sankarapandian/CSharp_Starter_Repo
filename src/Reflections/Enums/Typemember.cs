namespace Reflections.Enums;

/// <summary>
/// Denotes the kind of member of a type.
/// </summary>
public enum TypeMember
{
    /// <summary>
    /// Specifies the property of an type.
    /// </summary>
    Property,

    /// <summary>
    /// Specifies the field of an type.
    /// </summary>
    Field,

    /// <summary>
    /// Specifies the method of an type.
    /// </summary>
    Method,

    /// <summary>
    /// Specifies the event of an type.
    /// </summary>
    Event,
}
